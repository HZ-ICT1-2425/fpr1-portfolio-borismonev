    <h1 style="text-align: center">Frequently asked questions</h1>
    <ol>
        <li><strong>How can you print a document from your laptop at HZ?</strong>
            <p> Go to hz.mynetpay.nl and select "Mobility Print". Choose your current software and install the mobility
                print app. Select which file(s) you would like to print and choose the "HZ Printer". Find a printer and
                scan your student card or enter your HZ login details. Select "Print release", select your file, enter
                your preferred settings, and press print!
            </p>
        </li>

        <li><strong>How can you scan a document and send it to your laptop at HZ?</strong>
            <p> Log in to a printer using your HZ student card or username and password. Select the "scan" option and
                place the documents you'd like to scan on the feeder or glass plate. Select your preferred settings and
                press start. Remember to log out when finished.
            </p>
        </li>
        <li><strong>How can I buy something on the HZ web shop?</strong>
            <p> Go to webshop.hz.nl and use the navigation bar to select between "HZ", "HZ Cult", and "HZ Sport". Scroll
                down to view the available products, and select whichever ones you would like to purchase. Once you have
                selected everything, navigate to your shopping cart,select "checkoul" and complete the payment.
            </p>
        </li>
        <li><strong>How can you book a project space?</strong>
            <p> Log into MyHZ and select the selfservice portal in the personal menu. Select "Reservations" then
                "Reserve a meeting or project room" and finally "Reserve a room". On the top left you can select the
                date, and the top row displays the time. Click and drag to select a time slot in your preferred room,
                click next, fill in your information and confirm the reservation.
            </p>
        </li>
        <li><strong>What are the instructions if you want to park your car at the HZ parking lot?</strong>
            <p> It is not possible to park at the Groene Woud campus. The parking lot on Kousteensedijk has a number of
                spaces for visitors, staff and students of Het Groene Woud. This car park is an 8-minute walk from the
                HZ. When you show your entry card at the JRCZ service desk, located next to the HZ building, you can get
                an exit card.
            </p>
        </li>
    </ol>
</x-layout>
